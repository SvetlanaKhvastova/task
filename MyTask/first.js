// fib('7') => ''"
// fib(1) => "0"
// fib(0) => ''"
function fib(num) {
    if (typeof num !== "number" || num <= 0 || !Number.isInteger(num)) {
        return ""
    }

    let result = ""
    let first = 0;
    let second = 1;

    for (let i = 0; i < num; i++) {
        if (i + 1 === num) {
            result += `${first}`;
            // –ë–µ–∑ –ø—Ä–æ–±–µ–ª–∞ –≤ –∫–æ–Ω—Ü–µ
        } else {
            result += `${first} `;
        }

        let third = first + second;
        first = second;
        second = third;
    }

    return result
}

